#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	const char	*last = NULL;

	if (!s)
		return (NULL);
	while (*s)
	{
		if (*s == (char)c)
			last = s;
		s++;
	}
	if ((char)c == '\0')
		return ((char *)s);
	return ((char *)last);
}
